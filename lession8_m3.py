"""
Задание Nº3
На берегу реки стояли п рыбаков, все они хотели перебраться на другой берег. 
Одна лодка может выдержать не более т килограмм, при этом в лодку помещается не более 2 человек. 
Определите, какое минимальное число лодок нужно, чтобы перевезти на другой берег всех рыбаков 
В первую строку вводится число т (1 ≤ m ≤ 10е6) - максимальная масса, 
которую может выдержать одна лодка. Во вторую строку вводится число n (1 ≤ n ≤ 100) - количество рыбаков. 
В следующие N строк вводится по одному числу Ai (1 ≤ Ai ≤ m) - вес каждого путешественника. 
Программа должна вывести одно число - минимальное количество лодок, 
необходимое для переправки всех рыбаков на противоположный берег."""

# Вводим максимальную массу, которую может выдержать одна лодка
t = int(input("Введите максимальную массу, которую может выдержать одна лодка: "))

# Вводим количество рыбаков
n = int(input("Введите количество рыбаков: "))

# Инициализируем переменные для подсчета лодок и весов рыбаков
boats = 0
weights = {}

# Вводим вес каждого рыбака и сохраняем их в словарь
for i in range(n):
    weight = int(input("Введите вес рыбака {}: ".format(i+1)))
    if weight in weights:
        weights[weight] += 1
    else:
        weights[weight] = 1

# Проходим по всем весам рыбаков
for weight in weights:
    while weights[weight] > 0:
        # Уменьшаем количество рыбаков с текущим весом
        weights[weight] -= 1

        # Ищем партнера для текущего рыбака
        partner_weight = t - weight
        while partner_weight > 0:
            if partner_weight in weights and weights[partner_weight] > 0:
                weights[partner_weight] -= 1
                break
            partner_weight -= 1

        # Увеличиваем количество лодок
        boats += 1

# Выводим минимальное количество лодок
print("Минимальное количество лодок:", boats)
