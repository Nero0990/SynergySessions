"""
Задание Nº1
В первую строку вводится число N - количество чисел (1 ≤ N ≤ 100000). 
Во вторую строку вводится через пробел N чисел, каждое не превышает 2*10е9 по модулю. 
Требуется выяснить, сколько среди этих чисел различных.
Выведите число, равное количеству различных чисел среди данных."""

# Вводим количество чисел
N = int(input("Введите количество чисел: "))

# Вводим числа через пробел
numbers = input("Введите числа через пробел: ").split()

# Преобразуем числа в целые
numbers = [int(num) for num in numbers]

# Создаем пустой словарь для хранения уникальных чисел
unique_numbers = {}

# Проходим по всем числам
for num in numbers:
    if num not in unique_numbers:
        unique_numbers[num] = True

# Выводим количество уникальных чисел
print("Количество уникальных чисел:", len(unique_numbers))
