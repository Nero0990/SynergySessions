"""Задание №2
Дано пятизначное целое число. Напишите алгоритм, который возведёт количество десятков в степень количества единиц.
Затем умножит это число на количество сотен. 
И делит получившееся число на разность количества десятков тысяч и количества тысяч
Например, есть число 46275
Необходимо возвести 7 (десятки) в степень 5 (единицы), умножить получившееся число на 2 (сотни), 
и разделить на разность между 4 (десятки тысяч) и 6 (тысячи) то есть (4-6)
В результате необходимо получить вещественное число. В нашем примере это будет: - 16807.0
"""
# Запрашиваем у пользователя число
number = int(input("Введите число: "))

# Извлекаем цифры из числа
thousands = number // 10000
tens_of_thousands = (number // 1000) % 10
hundreds = (number // 100) % 10
tens = (number // 10) % 10
units = number % 10

# Проверяем, есть ли разность между десятками тысяч и тысячами
if tens_of_thousands - thousands == 0:
    print("Деление на ноль невозможно")
else:
    # Выполняем расчеты согласно заданию
    result = (tens ** units) * hundreds / (tens_of_thousands - thousands)
    print(f"Результат: {result}")
